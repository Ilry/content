local MOVESPEED = GetModConfigData("MOVESPEED")
local MUSIC = GetModConfigData("MUSIC")
local SLOWPERCENT = GetModConfigData("SLOWPERCENT")
local MAXSANITY_DAPPERNESS = GetModConfigData("MAXSANITY_DAPPERNESS")
