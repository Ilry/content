local GLOBAL = GLOBAL or _G
local TUNING = GLOBAL.TUNING or {}

local Brightness = GetModConfigData("Brightness")
local MODULE_USES = GetModConfigData("MODULE_USES")
local SCANNER_SCANPERIOD = GetModConfigData("SCANNER_SCANPERIOD")
local TASERDAMAGE = GetModConfigData("TASERDAMAGE")
local CHARGE_REGENTIME = GetModConfigData("CHARGE_REGENTIME")
local MINACCEPTABLEMOISTURE = GetModConfigData("MINACCEPTABLEMOISTURE")
