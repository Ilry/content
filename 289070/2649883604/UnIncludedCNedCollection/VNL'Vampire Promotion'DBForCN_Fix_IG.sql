UPDATE UnitPromotions
SET Name = 
	(CASE
		WHEN UnitPromotionType = "PROMOTION_VAMPIRE_ENTHRALL"
			THEN "{LOC_GONOPO_C8S_PROMOTION_VAMPIRE_ENTHRALL_NAME}"
		WHEN UnitPromotionType = "PROMOTION_VAMPIRE_BAT_SWARM"
			THEN "{LOC_GONOPO_C8S_PROMOTION_VAMPIRE_BAT_SWARM_NAME}"
		WHEN UnitPromotionType = "PROMOTION_VAMPIRE_DARK_GIFT"
			THEN "{LOC_GONOPO_C8S_PROMOTION_VAMPIRE_DARK_GIFT_NAME}"
		WHEN UnitPromotionType = "PROMOTION_VAMPIRE_EMISSARY"
			THEN "{LOC_GONOPO_C8S_PROMOTION_VAMPIRE_EMISSARY_NAME}"
		WHEN UnitPromotionType = "PROMOTION_VAMPIRE_BLOODLUST"
			THEN "{LOC_GONOPO_C8S_PROMOTION_VAMPIRE_BLOODLUST_NAME}"
		WHEN UnitPromotionType = "PROMOTION_VAMPIRE_MIST_CLOUD"
			THEN "{LOC_GONOPO_C8S_PROMOTION_VAMPIRE_MIST_CLOUD_NAME}"
		WHEN UnitPromotionType = "PROMOTION_VAMPIRE_IMMORTAL"
			THEN "{LOC_GONOPO_C8S_PROMOTION_VAMPIRE_IMMORTAL_NAME}"
	END)
WHERE UnitPromotionType IN
(
	"PROMOTION_VAMPIRE_ENTHRALL",
	"PROMOTION_VAMPIRE_BAT_SWARM",
	"PROMOTION_VAMPIRE_DARK_GIFT",
	"PROMOTION_VAMPIRE_EMISSARY",
	"PROMOTION_VAMPIRE_BLOODLUST",
	"PROMOTION_VAMPIRE_MIST_CLOUD",
	"PROMOTION_VAMPIRE_IMMORTAL"
);

